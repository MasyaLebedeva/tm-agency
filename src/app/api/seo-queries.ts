// Функция для получения случайных запросов (для ротации)
export function getRandomQueries(count: number = 3): string[] {
  const shuffled = [...seoQueries].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, Math.min(count, seoQueries.length))
}

// Функция для получения запросов по индексу (для последовательной публикации)
export function getQueriesByIndex(startIndex: number, count: number = 3): string[] {
  const result: string[] = []
  for (let i = 0; i < count; i++) {
    const index = (startIndex + i) % seoQueries.length
    result.push(seoQueries[index])
  }
  return result
}

